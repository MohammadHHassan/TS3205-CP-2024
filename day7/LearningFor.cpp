#include <stdio.h>				// Library for printf and scanf

int main(void)					// Main function
{
	int i;
	
	for(i=1 ; i<=10 ; i++)		// Starts from 1, Stops at 10, Increment by 1 each loop
	{
		printf("The number is %d\n", i);		// Print out the current number
	}
	
	printf("Thank you :)");
	
	return 0;					// End of main function
}
